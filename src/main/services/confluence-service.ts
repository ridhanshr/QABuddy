  private stripGeneratedMarkers(content: string): string {
    return content.replace(
      /\s*<p>\s*Generated by QA Buddy\s*[-—]\s*[^<]+<\/p>\s*/gi,
      "\n"
    );
  }

  private normalizeTableSpacing(content: string): string {
    return content
      .replace(/(?:\s*<p>\s*(?:<br\s*\/?>|\u00a0|&nbsp;)?\s*<\/p>\s*){2,}/gi, "\n<p><br /></p>\n")
      .replace(/(<\/table>)\s*(?:<p>\s*(?:<br\s*\/?>|\u00a0|&nbsp;)?\s*<\/p>\s*)+(?=<table\b)/gi, "$1\n<p><br /></p>\n")
      .replace(/^\s+|\s+$/g, "");
  }

  private collectParsedTables(content: string): Array<{ qaIndex: number; html: string; start: number; end: number; entry: any }> {
    const tables: Array<{ qaIndex: number; html: string; start: number; end: number; entry: any }> = [];
    const tableRegex = /<table[^>]*>[\s\S]*?<\/table>/gi;
    let tableMatch;
    let qaIndex = 0;

    while ((tableMatch = tableRegex.exec(content)) !== null) {
      const html = tableMatch[0];
      const parsed = this.parseSingleTable(html, false);
      if (parsed.entry && parsed.entry.testCaseNo) {
        tables.push({
          qaIndex,
          html,
          start: tableMatch.index,
          end: tableMatch.index + html.length,
          entry: parsed.entry,
        });
        qaIndex++;
      }
    }

    return tables;
  }

  private mergeEntriesIntoContent(
    content: string,
    entries: any[],
    jiraBaseUrl?: string,
    jiraServerId?: string,
    deletedTableIndices: number[] = []
  ): { content: string; replacedCount: number; appendedCount: number; deletedCount: number } {
    const sanitizedContent = this.normalizeTableSpacing(this.stripGeneratedMarkers(content));
    const parsedTables = this.collectParsedTables(sanitizedContent);
    const replacements = new Map<number, string>();
    const appendedTables: string[] = [];
    const deletedSet = new Set(deletedTableIndices);

    for (const entry of entries) {
      const tableHtml = generateSingleTable(entry, jiraBaseUrl, jiraServerId);
      if (typeof entry.sourceTableIndex === "number") {
        replacements.set(entry.sourceTableIndex, tableHtml);
      } else {
        appendedTables.push(tableHtml);
      }
    }

    if (replacements.size === 0 && appendedTables.length === 0 && deletedSet.size === 0) {
      return { content: sanitizedContent, replacedCount: 0, appendedCount: 0, deletedCount: 0 };
    }

    let nextContent = sanitizedContent;
    const targets = parsedTables
      .filter((table) => replacements.has(table.qaIndex) || deletedSet.has(table.qaIndex))
      .sort((a, b) => b.start - a.start);

    for (const table of targets) {
      if (deletedSet.has(table.qaIndex)) {
        const trailingBreakMatch = nextContent.slice(table.end).match(/^\s*<p><br\s*\/?><\/p>/i);
        const removeUntil = trailingBreakMatch ? table.end + trailingBreakMatch[0].length : table.end;
        nextContent = nextContent.slice(0, table.start) + nextContent.slice(removeUntil);
        continue;
      }
      nextContent =
        nextContent.slice(0, table.start) +
        replacements.get(table.qaIndex)! +
        nextContent.slice(table.end);
    }

    if (appendedTables.length > 0) {
      const appendedHtml = this.normalizeTableSpacing(appendedTables.join("\n<p><br /></p>\n"));
      nextContent = nextContent.trim() ? `${nextContent}\n<p><br /></p>\n${appendedHtml}` : appendedHtml;
    }

    nextContent = this.normalizeTableSpacing(nextContent);

    return {
      content: nextContent,
      replacedCount: targets.filter((table) => replacements.has(table.qaIndex)).length,
      appendedCount: appendedTables.length,
      deletedCount: targets.filter((table) => deletedSet.has(table.qaIndex)).length,
    };
  }

  async syncToConfluence(
    pageId: string,
    entries: any[],
    jiraBaseUrl?: string,
    jiraServerId?: string,
    deletedTableIndices: number[] = []
  ): Promise<{ pageTitle: string; pageUrl: string; entryCount: number; imageCount: number; attachmentCount: number }> {
    const page = await this.getPage(pageId);
    const currentVersion = page.version?.number || 1;
    const content = page.body?.storage?.value || "";

    const finalJiraServerId = jiraServerId || await this.resolveJiraServerId(content);

    let existingAttachmentNames: string[] = [];
    try {
      const attachments = await this.rawClient.getAttachments(pageId);
      existingAttachmentNames = attachments.map((att: any) => normalizeFilename(att.title));
    } catch (attErr) {
      logger.error("Confluence", "Failed to fetch existing attachments during sync:", attErr);
    }

    let imageCount = 0;
    let attachmentCount = 0;
    for (const entry of entries) {
      for (const img of normalizeAttachmentOrder(entry.images || [])) {
        if (!img.data) continue;

        const normalizedImgName = normalizeFilename(img.name);
        if (existingAttachmentNames.includes(normalizedImgName)) {
          continue;
        }

        try {
          await this.uploadAttachment(pageId, img.name, img.data);
          if (img.data.startsWith("data:image/")) {
            imageCount++;
          } else {
            attachmentCount++;
          }
        } catch (uploadErr) {
          logger.error("Confluence", `Failed to upload attachment ${img.name}:`, uploadErr);
          throw uploadErr;
        }
      }
    }

    const { content: bodyHtml, replacedCount, appendedCount, deletedCount } = this.mergeEntriesIntoContent(
      content,
      entries,
      jiraBaseUrl,
      finalJiraServerId,
      deletedTableIndices
    );

    const updated = await this.updatePage(pageId, page.title, bodyHtml, currentVersion);

    return {
      pageTitle: updated.title,
      pageUrl: `${this.config.baseUrl.replace(/\/+$/, "")}/spaces/${page.space?.key || ""}/pages/${pageId}`,
      entryCount: replacedCount + appendedCount + deletedCount,
      imageCount,
      attachmentCount,
    };
  }

  async previewSyncXhtml(
    pageId: string,
    entries: any[],
    jiraBaseUrl?: string,
    jiraServerId?: string
  ): Promise<{ currentTitle: string; currentVersion: number; generatedTables: string; entryCount: number; existingEntryCount: number }> {
    const page = await this.getPage(pageId);
    const currentVersion = page.version?.number || 1;
    const content = page.body?.storage?.value || "";

    const existingEntries = this.parseEntriesFromContent(content);
    const finalJiraServerId = jiraServerId || await this.resolveJiraServerId(content);
    const generatedTables = this.generateXhtmlTable(entries, jiraBaseUrl, finalJiraServerId);

    return {
      currentTitle: page.title,
      currentVersion,
      generatedTables,
      entryCount: entries.length,
      existingEntryCount: existingEntries.length,
    };
  }

  async getPageByUrl(url: string) {
    const pageIdMatch = url.match(/pages\/(\d+)/) || url.match(/pageId=(\d+)/);
    const pageId = pageIdMatch ? pageIdMatch[1] : "";
    if (pageId) {
      return this.getPage(pageId);
    }
    throw new Error("Invalid Confluence URL. Could not find page ID.");
  }

